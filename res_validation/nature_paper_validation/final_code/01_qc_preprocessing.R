seurat_list_filtered <- lapply(names(seurat_list), function(nm) {
  seu      <- seurat_list[[nm]]
  n_before <- ncol(seu)

  seu <- subset(seu,
                subset = nFeature_RNA >= 200 &
                         nFeature_RNA <= 6000 &
                         percent.mt   <  20)

  n_after  <- ncol(seu)
  n_remove <- n_before - n_after
  cat(sprintf("  %s: %d → %d cells (removed: %d, %.1f%%)\n",
              nm, n_before, n_after, n_remove, 100 * n_remove / n_before))

  qc_stats <<- rbind(qc_stats, data.frame(
    Sample_ID   = nm,
    Condition   = unique(seu$Condition),
    Pre_QCfilt  = n_before,
    Post_QCfilt = n_after,
    QC_removed  = n_remove,
    QC_pct      = round(100 * n_remove / n_before, 1)
  ))
  seu
})
