# 2. FLEXIBLE LOADING FUNCTION
#    Supports: MatrixMarket (.mtx), compressed CSV/TSV (.csv.gz, .tsv.gz)
# ==============================================================================
load_sample <- function(gsm_id, data_dir) {
  gsm_dir   <- file.path(data_dir, gsm_id)
  if (!dir.exists(gsm_dir)) {
    message(sprintf("  [SKIP] %s - directory not found", gsm_id)); return(NULL)
  }
  all_files <- list.files(gsm_dir, full.names = TRUE, recursive = TRUE)

  # Case 1: MatrixMarket (10x Genomics)
  mtx_file <- all_files[grepl("\\.mtx(\\.gz)?$", all_files, ignore.case = TRUE)]
  if (length(mtx_file) > 0) {
    barcodes_file <- all_files[grepl("barcodes", all_files, ignore.case = TRUE)]
    features_file <- all_files[grepl("features|genes", all_files, ignore.case = TRUE)]
    if (length(barcodes_file) > 0 && length(features_file) > 0) {
      tryCatch({
        counts <- ReadMtx(mtx = mtx_file[1], cells = barcodes_file[1],
                          features = features_file[1])
        message(sprintf("  [OK] %s - MatrixMarket (%d genes, %d cells)",
                        gsm_id, nrow(counts), ncol(counts)))
        return(counts)
      }, error = function(e) {
        message(sprintf("  [ERROR] %s - ReadMtx failed: %s", gsm_id, e$message))
        return(NULL)
      })
    }
  }

  # Case 2: Compressed CSV/TSV
  csv_files <- all_files[grepl("\\.(csv|tsv|txt)(\\.gz)?$", all_files, ignore.case = TRUE)]
  csv_files <- csv_files[!grepl("metadata|meta|barcode|feature|gene_name",
                                 basename(csv_files), ignore.case = TRUE)]
  if (length(csv_files) > 0) {
    tryCatch({
      sep_char <- ifelse(grepl("\\.tsv", csv_files[1]), "\t", ",")
      counts   <- read.table(gzfile(csv_files[1]), sep = sep_char,
                              header = TRUE, row.names = 1, check.names = FALSE)
      message(sprintf("  [OK] %s - CSV/TSV (%d genes, %d cells)",
                      gsm_id, nrow(counts), ncol(counts)))
      return(as.matrix(counts))
    }, error = function(e) {
      message(sprintf("  [ERROR] %s - CSV read failed: %s", gsm_id, e$message))
      return(NULL)
    })
  }

  message(sprintf("  [SKIP] %s - unrecognised format", gsm_id))
  return(NULL)
}

