  gsm_id    <- gsm_mapping$GSM_ID[i]
  sample_id <- gsm_mapping$Sample_ID[i]
  condition <- gsm_mapping$Condition[i]

  cat(sprintf("\n[%d/%d] %s (%s - %s)\n", i, nrow(gsm_mapping), gsm_id, sample_id, condition))
