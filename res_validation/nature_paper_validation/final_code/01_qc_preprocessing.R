# ==============================================================================
# 6. DOUBLET DETECTION WITH scDblFinder
#
# WHY: QC filtering based on nFeature alone does not reliably remove doublets.
#      scDblFinder explicitly models the expected count distribution of doublets
#      by simulating artificial doublets from real cells, then trains a random
#      forest classifier to assign a doublet score to each barcode.
#
# WHEN: Applied per sample independently, on raw (non-normalised) counts,
#       AFTER hard QC filtering and BEFORE normalization / merge.
#
# EXPECTED DOUBLET RATE: typically 2-10% depending on loading density.
#      For 10x Genomics: ~1% per 1,000 cells loaded.
#
# REFERENCE: Germain et al. (2021) F1000Research 10:979
# ==============================================================================
cat("\n--- Doublet detection with scDblFinder ---\n")
cat("  Running independently on each sample (raw counts, pre-normalization)\n")

doublet_stats <- data.frame()

seurat_list_clean <- lapply(names(seurat_list_filtered), function(nm) {
  seu      <- seurat_list_filtered[[nm]]
  n_before <- ncol(seu)
  cat(sprintf("  [%s] %d cells...", nm, n_before))

  # Convert Seurat → SingleCellExperiment (required by scDblFinder)
  sce <- tryCatch(
    as.SingleCellExperiment(seu),
    error = function(e) {
      message(sprintf(" [ERROR] SCE conversion: %s", e$message)); NULL
    }
  )

  if (is.null(sce)) {
    cat(" skipped, keeping all cells.\n")
    seu$doublet_class <- "singlet"; seu$doublet_score <- NA_real_
    return(seu)
  }

  sce <- tryCatch(
    scDblFinder(sce, samples = NULL, BPPARAM = SerialParam()),
    error = function(e) {
      message(sprintf(" [ERROR] scDblFinder: %s", e$message)); NULL
    }
  )

  if (is.null(sce)) {
    cat(" skipped, keeping all cells.\n")
    seu$doublet_class <- "singlet"; seu$doublet_score <- NA_real_
    return(seu)
  }

  # Transfer labels back to Seurat metadata
  seu$doublet_class <- sce$scDblFinder.class   # "singlet" | "doublet"
  seu$doublet_score <- sce$scDblFinder.score   # continuous score 0–1

  n_dbl <- sum(seu$doublet_class == "doublet")
  cat(sprintf(" %d doublets (%.1f%%)\n", n_dbl, 100 * n_dbl / n_before))

  # Remove doublets
  seu_clean <- subset(seu, subset = doublet_class == "singlet")

  doublet_stats <<- rbind(doublet_stats, data.frame(
    Sample_ID    = nm,
    Condition    = unique(seu$Condition),
    Pre_doublet  = n_before,
    Doublets     = n_dbl,
    Doublet_pct  = round(100 * n_dbl / n_before, 1),
    Post_doublet = ncol(seu_clean)
  ))

  seu_clean
})
