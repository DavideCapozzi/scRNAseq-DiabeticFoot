  }

  seu <- tryCatch(
    CreateSeuratObject(counts = counts, project = sample_id,
                       min.cells = 3, min.features = 200),
    error = function(e) { message("  CreateSeuratObject failed: ", e$message); NULL }
  )

  if (is.null(seu)) {
    load_stats <- rbind(load_stats, data.frame(
      GSM_ID = gsm_id, Sample_ID = sample_id, Condition = condition,
      Status = "SEURAT_FAIL", N_cells_raw = ncol(counts), N_genes = nrow(counts)))
    next
  }

  seu$orig.ident <- sample_id
  seu$GSM_ID     <- gsm_id
  seu$Condition  <- condition

  load_stats <- rbind(load_stats, data.frame(
    GSM_ID = gsm_id, Sample_ID = sample_id, Condition = condition,
    Status = "OK", N_cells_raw = ncol(seu), N_genes = nrow(seu)))

  seurat_list[[sample_id]] <- seu
